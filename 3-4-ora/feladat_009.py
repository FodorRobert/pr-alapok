szám2 = int(input("Írj be egy másik számot! "))
if szám1 > szám2:
    print('Az első szám nagyobb mint a második.')
elif szám2 > szám1:
    print("A második szám nagyobb mint az első.")
elif szám1 == szám2:
    print("A két szám egyenlő.")

